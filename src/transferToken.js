const contractAddress = process.env.TOKEN_CONTRACT_ADDRESS; // replace with your ERC20 token contract address
const contractABI = ERC20ABI; // replace with your ERC20 token contract ABI

const network = process.env.ALCHEMY_SEPOLIA_ENDPOINT; // replace with your Sepolia testnet API key
const provider = new ethers.providers.JsonRpcProvider(network);

async function transferTokens(recipientAddress, amount ,senderPrivateAddress) {
  const wallet = new ethers.Wallet(senderPrivateAddress  , provider);
  const tokenContract = new ethers.Contract(contractAddress, contractABI, wallet);




  // const gasLimit = await tokenContract.estimateGas.transfer(recipientAddress, amount) ;
  // const tx = await tokenContract.transfer(recipientAddress, amount, {  gasLimit: gasLimit * 4  });
  const gasPrice = ethers.utils.parseUnits('1000', 'gwei'); // replace with your desired gas price
  const gasLimit = await tokenContract.estimateGas.transfer(recipientAddress, amount);
  const tx = await tokenContract.transfer(recipientAddress, amount, { gasLimit:gasLimit*20, gasPrice });
  console.log(tx)
  let result = await tx.wait();
  return result.transactionHash
}

  module.exports.transferTokens=transferTokens